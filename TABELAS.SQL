  IDESTABELECIMENTO INT IDENTITY
 ,IDPESSOA INT NULL
 ,IDCATEGORIA INT NULL
 ,DATACADASTRO DATE NULL
 ,STATUS VARCHAR(10) NULL
 ,AGENCIA VARCHAR(6) NULL
 ,CONTACORRENTE VARCHAR(10) NULL
 ,PRIMARY KEY CLUSTERED (IDESTABELECIMENTO)
